    private void removeEntryButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeEntryButtonMouseClicked
        int row = stockTable.getSelectedRow();
        if (row == -1) {
            HighlightDialog dialog = new HighlightDialog(this, true);
            dialog.setLocation(700, 400);
            dialog.setVisible(true);
        } else {

            DefaultTableModel model = (DefaultTableModel) stockTable.getModel();

            if (stockTable.getSelectedRowCount() == 1) {

                //delete the corresponding record from catalog file
                String currentDirectory = System.getProperty("user.dir");
                currentDirectory.replace("'\'", "\\");
                File catalog = new File(currentDirectory + "\\catalog.txt");
                removeRecord(catalog.getPath(), model.getValueAt(stockTable.getSelectedRow(), 0).toString());
                File fileToRemove = new File(currentDirectory + "\\" + model.getValueAt(stockTable.getSelectedRow(), 0).toString() + ".txt");
                fileToRemove.delete();

                //remove the selected row
                model.removeRow(stockTable.getSelectedRow());

            } else {
                TooManyRowsSelectedDialog dialog = new TooManyRowsSelectedDialog(this, true);
                dialog.setLocation(700, 400);
                dialog.setVisible(true);
            }

            
            HomePage home = new HomePage();
            setVisible(false);
            home.setVisible(true);
            dispose();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_removeEntryButtonMouseClicked

