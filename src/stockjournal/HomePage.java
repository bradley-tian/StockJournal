    public void populateStockTable() {
        //create or locate catalog file in user's local drive
        String currentDirectory = System.getProperty("user.dir");
        currentDirectory.replace("'\'", "\\");
        File catalog = new File(currentDirectory + "\\catalog.txt");

        //read data from catalog file
        ArrayList<String> records = new ArrayList<>();
        String delimiter = "[|\n]";
        records = readRecordsFromCatalog(records, catalog, delimiter);

        //populate stock table with data from catalog file
        DefaultTableModel model = (DefaultTableModel) stockTable.getModel();
        Object rowData[] = new Object[3];
        for (int i = 0; i < records.size(); i += 3) {
            rowData[0] = records.get(i);
            rowData[1] = records.get(i + 1);
            rowData[2] = records.get(i + 2);
            model.addRow(rowData);
            rowData = new Object[3];
        }
    }

    private static ArrayList<String> readRecordsFromCatalog(ArrayList<String> records, File file, String delimiter) {
        try {
            Scanner reader = new Scanner(file);
            reader.useDelimiter(delimiter);

            while (reader.hasNext()) {
                String s = reader.next();
                records.add(s);
            }
            reader.close();
            return records;
        } catch (FileNotFoundException e) {
            System.out.println("Error: file not found.");
            return records;
        }
    }

    private static void removeRecord(String file, String term) {
        String temp = "temp.txt";
        File oldFile = new File(file);
        File newFile = new File(temp);
        String Name = "";
        String Ticker = "";
        String Exchange = "";

        try {
            FileWriter fw = new FileWriter(temp, true);
            BufferedWriter bw = new BufferedWriter(fw);
            PrintWriter pw = new PrintWriter(bw);
            Scanner reader = new Scanner(new File(file));
            reader.useDelimiter("[|\n]"); //identifying splitting signals

            while (reader.hasNext()) {
                Name = reader.next();
                Ticker = reader.next();
                Exchange = reader.next();
                if (!Name.equals(term)) { //while the removal target isn't scanned 
                    pw.println(Name + "|" + Ticker + "|" + Exchange);
                }
            }
            reader.close();
            pw.flush();
            pw.close();
            oldFile.delete();
            File dump = new File(file);
            newFile.renameTo(dump); //sequence to write data into new file and replace the old one

        } catch (IOException e) {
            System.out.println("Error.");
        }
    }
    
